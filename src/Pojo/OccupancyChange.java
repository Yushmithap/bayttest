package Pojo;

public class OccupancyChange {
	
	private String occupancy;
	private String current_sq_ft;
	private String previous_sq_ft;
	private String change;
	private String current_number_of_rooms;
	private String previous_number_of_rooms;
	
	public String getCurrent_number_of_rooms() {
		return current_number_of_rooms;
	}
	public void setCurrent_number_of_rooms(String current_number_of_rooms) {
		this.current_number_of_rooms = current_number_of_rooms;
	}
	public String getPrevious_number_of_rooms() {
		return previous_number_of_rooms;
	}
	public void setPrevious_number_of_rooms(String previous_number_of_rooms) {
		this.previous_number_of_rooms = previous_number_of_rooms;
	}
	public String getOccupancy() {
		return occupancy;
	}
	public void setOccupancy(String occupancy) {
		this.occupancy = occupancy;
	}
	public String getCurrent_sq_ft() {
		return current_sq_ft;
	}
	public void setCurrent_sq_ft(String current_sq_ft) {
		this.current_sq_ft = current_sq_ft;
	}
	public String getPrevious_sq_ft() {
		return previous_sq_ft;
	}
	public void setPrevious_sq_ft(String previous_sq_ft) {
		this.previous_sq_ft = previous_sq_ft;
	}
	public String getChange() {
		return change;
	}
	public void setChange(String change) {
		this.change = change;
	}
	
	

}
