package Pojo;

public class VehicleDetails {

	private String vin;
	private String make_model;
	private String type;
	private String physical_damage;
	private String collision;
	private String seat_capacity;
	private String operating_radius;
	private String drive_to_work;
	private String vehicle_use;
	private String cost;
	private String premium;
	
	public String getVin() {
		return vin;
	}
	public void setVin(String vin) {
		this.vin = vin;
	}
	public String getMake_model() {
		return make_model;
	}
	public void setMake_model(String make_model) {
		this.make_model = make_model;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getPhysical_damage() {
		return physical_damage;
	}
	public void setPhysical_damage(String physical_damage) {
		this.physical_damage = physical_damage;
	}
	public String getCollision() {
		return collision;
	}
	public void setCollision(String collision) {
		this.collision = collision;
	}
	public String getSeat_capacity() {
		return seat_capacity;
	}
	public void setSeat_capacity(String seat_capacity) {
		this.seat_capacity = seat_capacity;
	}
	public String getOperating_radius() {
		return operating_radius;
	}
	public void setOperating_radius(String operating_radius) {
		this.operating_radius = operating_radius;
	}
	public String getDrive_to_work() {
		return drive_to_work;
	}
	public void setDrive_to_work(String drive_to_work) {
		this.drive_to_work = drive_to_work;
	}
	public String getVehicle_use() {
		return vehicle_use;
	}
	public void setVehicle_use(String vehicle_use) {
		this.vehicle_use = vehicle_use;
	}
	public String getCost() {
		return cost;
	}
	public void setCost(String cost) {
		this.cost = cost;
	}
	public String getPremium() {
		return premium;
	}
	public void setPremium(String premium) {
		this.premium = premium;
	}
	
	
	

}
