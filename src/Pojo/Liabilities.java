package Pojo;

public class Liabilities {
	
	private String account_payable;
	private String other_current_liabilities;
	private String total_current_liabilities;
	private String long_term_debt;
	private String total_long_term_liabilities;
	private String total_liabilities;
	
	public String getAccount_payable() {
		return account_payable;
	}
	public void setAccount_payable(String account_payable) {
		this.account_payable = account_payable;
	}
	public String getOther_current_liabilities() {
		return other_current_liabilities;
	}
	public void setOther_current_liabilities(String other_current_liabilities) {
		this.other_current_liabilities = other_current_liabilities;
	}
	public String getTotal_current_liabilities() {
		return total_current_liabilities;
	}
	public void setTotal_current_liabilities(String total_current_liabilities) {
		this.total_current_liabilities = total_current_liabilities;
	}
	public String getLong_term_debt() {
		return long_term_debt;
	}
	public void setLong_term_debt(String long_term_debt) {
		this.long_term_debt = long_term_debt;
	}
	public String getTotal_long_term_liabilities() {
		return total_long_term_liabilities;
	}
	public void setTotal_long_term_liabilities(String total_long_term_liabilities) {
		this.total_long_term_liabilities = total_long_term_liabilities;
	}
	public String getTotal_liabilities() {
		return total_liabilities;
	}
	public void setTotal_liabilities(String total_liabilities) {
		this.total_liabilities = total_liabilities;
	}

}
