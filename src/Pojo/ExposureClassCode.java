package Pojo;

public class ExposureClassCode {
	
	private String class_code;
	private String current_payroll;
	private String previous_payroll;
	private String change;
	private String current_head_count;
	private String previous_head_count;

	
	public String getClass_code() {
		return class_code;
	}
	public void setClass_code(String class_code) {
		this.class_code = class_code;
	}
	public String getCurrent_payroll() {
		return current_payroll;
	}
	public void setCurrent_payroll(String current_payroll) {
		this.current_payroll = current_payroll;
	}
	public String getPrevious_payroll() {
		return previous_payroll;
	}
	public void setPrevious_payroll(String previous_payroll) {
		this.previous_payroll = previous_payroll;
	}
	public String getChange() {
		return change;
	}
	public void setChange(String change) {
		this.change = change;
	}
	public String getCurrent_head_count() {
		return current_head_count;
	}
	public void setCurrent_head_count(String current_head_count) {
		this.current_head_count = current_head_count;
	}
	public String getPrevious_head_count() {
		return previous_head_count;
	}
	public void setPrevious_head_count(String previous_head_count) {
		this.previous_head_count = previous_head_count;
	}
	
	

}
