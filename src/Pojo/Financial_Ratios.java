package Pojo;

public class Financial_Ratios {
	
	private String current_ratio;
	private String acid_test_ratio;
	private String total_indebtedness;
	private String current_liabilities_over_net_worth;
	private String liabilities_over_net_worth;
	private String quick_ratio;
	
	public String getCurrent_ratio() {
		return current_ratio;
	}
	public void setCurrent_ratio(String current_ratio) {
		this.current_ratio = current_ratio;
	}
	public String getAcid_test_ratio() {
		return acid_test_ratio;
	}
	public void setAcid_test_ratio(String acid_test_ratio) {
		this.acid_test_ratio = acid_test_ratio;
	}
	public String getTotal_indebtedness() {
		return total_indebtedness;
	}
	public void setTotal_indebtedness(String total_indebtedness) {
		this.total_indebtedness = total_indebtedness;
	}
	public String getCurrent_liabilities_over_net_worth() {
		return current_liabilities_over_net_worth;
	}
	public void setCurrent_liabilities_over_net_worth(String current_liabilities_over_net_worth) {
		this.current_liabilities_over_net_worth = current_liabilities_over_net_worth;
	}
	public String getLiabilities_over_net_worth() {
		return liabilities_over_net_worth;
	}
	public void setLiabilities_over_net_worth(String liabilities_over_net_worth) {
		this.liabilities_over_net_worth = liabilities_over_net_worth;
	}
	public String getQuick_ratio() {
		return quick_ratio;
	}
	public void setQuick_ratio(String quick_ratio) {
		this.quick_ratio = quick_ratio;
	}

}
