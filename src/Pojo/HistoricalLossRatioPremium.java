package Pojo;

public class HistoricalLossRatioPremium {
	
	private String policy_year;
	private String premium_paid;
	private String loss_ratio;
	private String combined_ratio;
	
	public String getPolicy_year() {
		return policy_year;
	}
	public void setPolicy_year(String policy_year) {
		this.policy_year = policy_year;
	}
	public String getPremium_paid() {
		return premium_paid;
	}
	public void setPremium_paid(String premium_paid) {
		this.premium_paid = premium_paid;
	}
	public String getLoss_ratio() {
		return loss_ratio;
	}
	public void setLoss_ratio(String loss_ratio) {
		this.loss_ratio = loss_ratio;
	}
	public String getCombined_ratio() {
		return combined_ratio;
	}
	public void setCombined_ratio(String combined_ratio) {
		this.combined_ratio = combined_ratio;
	}
	

}
