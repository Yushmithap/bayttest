package Pojo;

public class HistoricalLossRun {
	
	private String policy_year;
	private String total_open_claims;
	private String total_closed_claims;
	private String total_paid;
	private String total_recovered;
	private String total_outstanding;
	private String total_incurred;
	
	public String getPolicy_year() {
		return policy_year;
	}
	public void setPolicy_year(String policy_year) {
		this.policy_year = policy_year;
	}
	public String getTotal_open_claims() {
		return total_open_claims;
	}
	public void setTotal_open_claims(String total_open_claims) {
		this.total_open_claims = total_open_claims;
	}
	public String getTotal_closed_claims() {
		return total_closed_claims;
	}
	public void setTotal_closed_claims(String total_closed_claims) {
		this.total_closed_claims = total_closed_claims;
	}
	public String getTotal_paid() {
		return total_paid;
	}
	public void setTotal_paid(String total_paid) {
		this.total_paid = total_paid;
	}
	public String getTotal_recovered() {
		return total_recovered;
	}
	public void setTotal_recovered(String total_recovered) {
		this.total_recovered = total_recovered;
	}
	public String getTotal_outstanding() {
		return total_outstanding;
	}
	public void setTotal_outstanding(String total_outstanding) {
		this.total_outstanding = total_outstanding;
	}
	public String getTotal_incurred() {
		return total_incurred;
	}
	public void setTotal_incurred(String total_incurred) {
		this.total_incurred = total_incurred;
	}

}
