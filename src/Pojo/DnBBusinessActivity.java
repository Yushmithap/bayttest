package Pojo;

public class DnBBusinessActivity {
	
	private String reporting_date;
	private String description;
	private String financial_status;
	private String financial_condition;
	private String facility;
	private String location;
	private String seasonality;
	
	public String getReporting_date() {
		return reporting_date;
	}
	public void setReporting_date(String reporting_date) {
		this.reporting_date = reporting_date;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getFinancial_status() {
		return financial_status;
	}
	public void setFinancial_status(String financial_status) {
		this.financial_status = financial_status;
	}
	public String getFinancial_condition() {
		return financial_condition;
	}
	public void setFinancial_condition(String financial_condition) {
		this.financial_condition = financial_condition;
	}
	public String getFacility() {
		return facility;
	}
	public void setFacility(String facility) {
		this.facility = facility;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	public String getSeasonality() {
		return seasonality;
	}
	public void setSeasonality(String seasonality) {
		this.seasonality = seasonality;
	}
	

}
