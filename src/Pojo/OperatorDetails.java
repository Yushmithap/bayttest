package Pojo;

public class OperatorDetails {
	
	public String operator_name;
	public String operator_licence_id;
	public String operator_age;
	public String years_of_experience;
	public String violation_count;
	
	public String getOperator_name() {
		return operator_name;
	}
	public void setOperator_name(String operator_name) {
		this.operator_name = operator_name;
	}
	public String getOperator_licence_id() {
		return operator_licence_id;
	}
	public void setOperator_licence_id(String operator_licence_id) {
		this.operator_licence_id = operator_licence_id;
	}
	public String getOperator_age() {
		return operator_age;
	}
	public void setOperator_age(String operator_age) {
		this.operator_age = operator_age;
	}
	public String getYears_of_experience() {
		return years_of_experience;
	}
	public void setYears_of_experience(String years_of_experience) {
		this.years_of_experience = years_of_experience;
	}
	public String getViolation_count() {
		return violation_count;
	}
	public void setViolation_count(String violation_count) {
		this.violation_count = violation_count;
	}
	

}
