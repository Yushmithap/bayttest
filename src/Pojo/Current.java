package Pojo;

public class Current {
	
	private String named_insured;
	private String account_id;
	private String request_type;
	private String year;
	private String agency_brokerage_firm;
	private String contact_name;
	private String address;
	private String contact;
	private String phone;
	private String fax;
	
	public String getNamed_insured() {
		return named_insured;
	}
	public void setNamed_insured(String named_insured) {
		this.named_insured = named_insured;
	}
	public String getAccount_id() {
		return account_id;
	}
	public void setAccount_id(String account_id) {
		this.account_id = account_id;
	}
	public String getRequest_type() {
		return request_type;
	}
	public void setRequest_type(String request_type) {
		this.request_type = request_type;
	}
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getAgency_brokerage_firm() {
		return agency_brokerage_firm;
	}
	public void setAgency_brokerage_firm(String agency_brokerage_firm) {
		this.agency_brokerage_firm = agency_brokerage_firm;
	}
	public String getContact_name() {
		return contact_name;
	}
	public void setContact_name(String contact_name) {
		this.contact_name = contact_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getContact() {
		return contact;
	}
	public void setContact(String contact) {
		this.contact = contact;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getFax() {
		return fax;
	}
	public void setFax(String fax) {
		this.fax = fax;
	}
	

}
