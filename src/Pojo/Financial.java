package Pojo;

public class Financial {
	
	private String sales_revenues; 
	private String cost_of_sales ;
	private String working_capital ;
	private String dividends;
	private String gross_profit ;
	private String profit_before_taxes ;
	private String operating_profit ;
	private String net_worth ;
	private String net_income ;
	private String net_assets ;
	
	public String getSales_revenues() {
		return sales_revenues;
	}
	public void setSales_revenues(String sales_revenues) {
		this.sales_revenues = sales_revenues;
	}
	public String getCost_of_sales() {
		return cost_of_sales;
	}
	public void setCost_of_sales(String cost_of_sales) {
		this.cost_of_sales = cost_of_sales;
	}
	public String getWorking_capital() {
		return working_capital;
	}
	public void setWorking_capital(String working_capital) {
		this.working_capital = working_capital;
	}
	public String getDividends() {
		return dividends;
	}
	public void setDividends(String dividends) {
		this.dividends = dividends;
	}
	public String getGross_profit() {
		return gross_profit;
	}
	public void setGross_profit(String gross_profit) {
		this.gross_profit = gross_profit;
	}
	public String getProfit_before_taxes() {
		return profit_before_taxes;
	}
	public void setProfit_before_taxes(String profit_before_taxes) {
		this.profit_before_taxes = profit_before_taxes;
	}
	public String getOperating_profit() {
		return operating_profit;
	}
	public void setOperating_profit(String operating_profit) {
		this.operating_profit = operating_profit;
	}
	public String getNet_worth() {
		return net_worth;
	}
	public void setNet_worth(String net_worth) {
		this.net_worth = net_worth;
	}
	public String getNet_income() {
		return net_income;
	}
	public void setNet_income(String net_income) {
		this.net_income = net_income;
	}
	public String getNet_assets() {
		return net_assets;
	}
	public void setNet_assets(String net_assets) {
		this.net_assets = net_assets;
	} 

}
