package Pojo;

public class DnBRating {
	
	private String rating;
	private String financialStrength;
	private String riskSegment;
	
	public String getRating() {
		return rating;
	}
	public void setRating(String rating) {
		this.rating = rating;
	}
	public String getFinancialStrength() {
		return financialStrength;
	}
	public void setFinancialStrength(String financialStrength) {
		this.financialStrength = financialStrength;
	}
	public String getRiskSegment() {
		return riskSegment;
	}
	public void setRiskSegment(String riskSegment) {
		this.riskSegment = riskSegment;
	}
	
	

}
